#include<iostream>
#include<string>
#include<cmath>

using namespace std;

int main(){

    double n;

    cout<<"Enter a floating point number\n";
    cin>>n;
    cout<<"n == "<<n<<endl;
    cout<<"n + 1 = "<<n+1<<endl;
    cout<<"n * 3 = "<<n*3<<endl;
    cout<<"n * n = "<<n*n<<endl;
    cout<<"n / 2 = "<<n/2<<endl;
    cout<<"square-root of n == "<<sqrt(n)<<endl;

    return(0);
}